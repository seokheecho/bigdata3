/* 비교 연산자: ==, !=, <, <=, >, >=
 * => 비교 연산의 결과는 true 또는 false이다. 
 */
package step04;

public class Test02_1 {
  public static void main(String[] args) {
    int a = 5, b = 2;
    System.out.println(a == b);
    System.out.println(a != b);
    System.out.println(a < b);
    System.out.println(a <= b);
    System.out.println(a > b);
    System.out.println(a >= b);
  }
}
