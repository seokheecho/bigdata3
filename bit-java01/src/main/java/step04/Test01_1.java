/* 산술 연산자: +, -, *, /, % 
 */
package step04;

public class Test01_1 {
  public static void main(String[] args) {
    int a = 5, b = 2;
    System.out.println(a + b);
    System.out.println(a - b);
    System.out.println(a * b);
    System.out.println(a / b); // = 2. int와 int의 연산결과는 int이다.
    System.out.println(a % b);
  }
}
