/* 자바 코드에서 값을 표현하는 방법 - "리터럴(literal)"
 * => 숫자, 부동소수점, 문자, 논리값, 문자열  
 */
package step01;

public class Test02 {
  public static void main(String[] args) {
    System.out.println("문자열"); // 문자열은 큰 따옴표를 사용해야 한다.
    System.out.println(100); //숫자
    System.out.println(3.14); // 부동소수점
    System.out.println('A'); // 문자는 작은 따옴표를 사용해야 한다.
    System.out.println("A"); // 문자가 아니라 문자열이다.
    System.out.println(true); // 반드시 소문자로 true 또는 false 를 작성해야 한다.
    System.out.println(false); 

  }

}
 