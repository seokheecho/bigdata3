/* 명령어의 실행 흐름 제어: 반복문 for 와 배열
 * => 배열을 반복할 때 주로 사용한다.
 */
package step05;

public class Test05_6 {

  public static void main(String[] args) {
    int[] arr1 = {10, 20, 30, 40, 50, 60, 70};
    
    for (int i = 0; i < arr1.length; i++) {
      System.out.println(arr1[i]);
    }

  } 

}









