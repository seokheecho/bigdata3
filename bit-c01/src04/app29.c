/* 연산자 - 관계 연산자(<, <=, >, >=, ==, !=)
 */

#include <stdio.h>
#include <stdlib.h>
#include <string.h>

int main29(void) {
	printf("5 < 2 = %d\n", 5 < 2);
	printf("5 <= 2 = %d\n", 5 <= 2);
	printf("5 > 2 = %d\n", 5 > 2);
	printf("5 >= 2 = %d\n", 5 >= 2);
	printf("5 == 2 = %d\n", 5 == 2);
	printf("5 != 2 = %d\n", 5 != 2);

	return EXIT_SUCCESS;
}







