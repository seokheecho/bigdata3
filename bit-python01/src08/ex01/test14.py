# 반복문 - continue, break

print("1-------------------------")

# continue

count = 0
while count < 10:
    count += 1
    if count == 5:
        continue
    print(count)

print("2-------------------------")

# break

count = 0
while count < 10:
    count += 1
    if count == 5:
        break
    print(count)
    
    
    
    
    
    
    